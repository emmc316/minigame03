#ifndef MAP_H_INCLUDED
#define MAP_H_INCLUDED

class Map
{

public:
    static const int row = 10;
    static const int column = 10;

    Map()
    {
    }

    char mapGame[row][column] = {{'*','*','*','*','*','*','*','*','*','*',},
        {'*','*','*','*','*','*','*','*','*','*',},
        {'*','*','*','*','*','*','*','*','*','*',},
        {'*','*','*','*','*','*','*','*','*','*',},
        {'*','*','*','*','*','*','*','*','*','*',},
        {'*','*','*','*','*','*','*','*','*','*',},
        {'*','*','*','*','*','*','*','*','*','*',},
        {'*','*','*','*','*','*','*','*','*','*',},
        {'*','*','*','*','*','*','*','*','*','*',},
        {'*','*','*','*','*','*','*','*','*','*'}
    };


    void printMap()
    {
        for(int i = 0; i < row; i++ )
        {
            for(int j = 0; j < column; j++)
            {
                std::cout<<mapGame[i][j]<<"    ";
            }
            std::cout<<"\n\n\n";
        }

    }

};


#endif // MAP_H_INCLUDED
